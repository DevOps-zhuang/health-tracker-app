/**
 * Login page countdown timer
 * This script handles the countdown functionality on the login page
 */

// Function to start the countdown from 30 seconds
function startCountdown() {
    var countdownElement = document.getElementById('countdown');
    var timeLeft = 30;

    // Update the countdown display immediately
    countdownElement.innerHTML = timeLeft;

    // Set interval to update the countdown every second
    var countdownTimer = setInterval(function() {
        // Decrease time left
        timeLeft -= 1;
        
        if (timeLeft <= 0) {
            // When countdown reaches zero
            clearInterval(countdownTimer);
            countdownElement.innerHTML = '0';
            
            // Optionally redirect or show a message when time is up
            alert('Your session has expired. Please refresh the page.');
        } else {
            // Update the display with current time left
            countdownElement.innerHTML = timeLeft;
        }
    }, 1000);
}

// Start the countdown when the page loads
document.addEventListener('DOMContentLoaded', function() {
    startCountdown();
});

